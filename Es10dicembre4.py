Latoquadrato = float (input ("inserisci misura del lato"))
Areaquadrato = Latoquadrato ** 2
print ("questa è l'area del quadrato:", Areaquadrato)

Altezzarettangolo = float (input ("inserisci altezza rettangolo"))
Baserettangolo = float (input ("inserisci base"))
Arearettangolo = Altezzarettangolo * Baserettangolo
print ("questa è l'area del rettangolo:", Arearettangolo)

Altezzatriangolo = float (input ("inserisci altezza triangolo"))
Basetriangolo = float (input ("inserisci base triangolo"))
Areatriangolo = (Altezzatriangolo * Basetriangolo) / 2
print ("questa è l'area del triangolo:", Areatriangolo)

Raggiocerchio = float (input ("inserisci raggio triangolo "))
